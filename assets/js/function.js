function addTwoNumbers(a,b) {
    console.log(a+b)
    return a+b;
}

function getSqRoot(a) {
    console.log(Math.sqrt(a))
    return Math.sqrt(a);
}

function getCercumference(r) {
    //2*PI*r
    console.log(2*Math.PI*r)
    return 2*Math.PI*r;
}

